// Example using array
// You can name your variables anything
let [a, b] = [1,2];
console.log(a);
console.log(b);

// Example using object
// Your name of the variables should match the name of the properties
// Order does not matter
let {d , c} = {
    c: 1,
    d: 2
}
console.log(c);    // 1
console.log(d);    // 2
