// Equality (==)
let a = 5;
let b = 6;
console.log(7 == 7);
console.log(a  == b);

// Strict equality
console.log(5 === 5);
console.log(5 === 6);
console.log(6 === '6');
console.log('7' === '7');

// Inequality (!=)
console.log(5 != 3);
console.log(3 != 3);

// Strict inequality (!==)
console.log(5 !== 5);
console.log(3 !== '3');

// Greater than (>)
// Less than (<)
console.log(5 > 3);
console.log(5 > 7);
console.log(5 < 3);
console.log(5 < 3);

// Greater than or equal to
// Less than or equal to
console.log(5 >= 5);
console.log(5 <= 3);
console.log(5 >= 3);