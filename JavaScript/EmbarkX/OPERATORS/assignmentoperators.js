// Assignment (=)
let a = 5;
console.log("a:", a);

// Addition assignment (+=)
let b = 10;
b += 3;    // Equivalent to : b = b + 3;
console.log("b:", b);

// Subtraction assignment (-=)
let c = 15;
c -= 5;    // Equivalent to: c = c - 5;
console.log("c:", c);

// Multiplication assignment (*=)
let d = 6;
d *= 2;    // Equivalent to: d = d * 2;
console.log("d:", d);

// Division assignment
let e = 20;
e /= 4;    // Equivalent to: e = e / 4;
console.log("e:", e);