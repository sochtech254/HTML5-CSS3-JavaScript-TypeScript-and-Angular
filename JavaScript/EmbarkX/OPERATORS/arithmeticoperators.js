// Addition (+)
let additionResult = 5 + 3;
console.log("Addition Result:", additionResult);

// Subtraction
let subtractionResult = 10 - 3;
console.log("Subtraction Result:", subtractionResult);

// Multiplication
let multiplicationResult = 4 * 6;
console.log("Multiplication Result:", multiplicationResult);

// Division
let divisionResult = 20 / 4;
console.log("Division Result:", divisionResult);

// Modulus
let modulusResult = 10 % 6;
console.log("Modulus Result:", modulusResult);

// Increment (++)
let x = 5;
x++;
console.log("Increment Result:", x);

// Decrement (--)
let y = 9;
y--;
console.log("Decrement Result:", y);