// For Loops:
for (let i = 0; i < 10; i++) {
    console.log(i);
}

for (let i = 10; i > 0; i--) {
    console.log(i);
}

let numbers = [11, 12, 13, 14, 15, 16, 17, 18];
for (let i = 0; i < numbers.length; i++) {
    console.log(i);
}

// nested for loop - matrix
for (let i = 0; i < 10; i++) {
    for (let j = 0; j < i; j++) {
        console.table(j);
    }
    console.log("\n");
}

